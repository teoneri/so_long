#include "../ft_printf.h"

int	ft_countdigits(long long int n)
{
	unsigned int	res;

	res = 0;
	if (n == 0)
		return (1);
	if (n < 0)
		res++;
	while (n != 0)
	{
		n /= 10;
		res++;
	}
	return (res);
}
